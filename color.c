#include "ft_ls.h"

void	yellow(void)
{
	write(1, "\033[1;33m", 7);
}

void	blue(void)
{
	write(1, "\033[0;34m", 7);
}

void	green(void)
{
	write(1, "\033[0;32m", 7);
}

void	cyan(void)
{
	write(1, "\033[0;36m", 7);
}

void	reset(void)
{
	write(1, "\033[0m", 4);
}
